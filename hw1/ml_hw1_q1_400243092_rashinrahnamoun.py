import numpy as np
import pandas as pd
from collections import Counter


class DecisionTree:
    def __init__(self, max_depth=None, min_samples_split=2, criterion='entropy'):
        self.md = max_depth
        self.ms = min_samples_split
        self.cr = criterion
        self.tr = None
        self.mc = None

    def calculate_entropy(self, y):
        cc = Counter(y)
        t = len(y)
        return -sum((c/t) * np.log2(c/t) for c in cc.values())

    def calculate_gini(self, y):
        cc = Counter(y)
        t = len(y)
        return 1 - sum((c/t)**2 for c in cc.values())

    def _get_split_gain(self, p, yl, yr):
        nl, nr, nt = len(yl), len(yr), len(yl) + len(yr)
        if self.cr == 'entropy':
            g = p - (nl/nt * self.calculate_entropy(yl) + nr/nt * self.calculate_entropy(yr))
        else:
            g = p - (nl/nt * self.calculate_gini(yl) + nr/nt * self.calculate_gini(yr))
        return g

    def _best_split(self, xc, y):
        u = np.unique(xc)
        if len(u) < 2:
            return (None, None)
        p = self.calculate_entropy(y) if self.cr == 'entropy' else self.calculate_gini(y)
        if len(u) > 10:
            si = np.argsort(xc)
            sx, sy = xc[si], y[si]
            sp = (u[:-1] + u[1:]) / 2
            bg = -float('inf')
            bs = None
            for s in sp:
                li = np.searchsorted(sx, s, side='left')
                yl, yr = sy[:li], sy[li:]
                if len(yl) == 0 or len(yr) == 0:
                    continue
                g = self._get_split_gain(p, yl, yr)
                if g > bg:
                    bg = g
                    bs = s
        else:
            bg = p
            bs = None
            for v in u:
                sb = y[xc == v]
                g = p - (len(sb)/len(y) * (self.calculate_entropy(sb) if self.cr == 'entropy' else self.calculate_gini(sb)))
                if g > bg:
                    bg = g
                    bs = v
        return (bs, bg)

    def best_split(self, X, y):
      bf, bg, bs, bn = None, -float('inf'), None, False
      for f in range(X.shape[1]):
          sv, g = self._best_split(X[:, f], y)
          if g is not None and g > bg:
              bg, bf, bs, bn = g, f, sv, len(np.unique(X[:, f])) > 10
      return (bf, bs, bn)


    def build_tree(self, X, y, d=0):
        if len(np.unique(y)) == 1 or (self.md is not None and d >= self.md) or (len(y) < self.ms):
            return np.argmax(np.bincount(y))
        f, sv, bn = self.best_split(X, y)
        if f is None:
            return np.argmax(np.bincount(y))
        n = {'feature': f, 'split_val': sv, 'is_numerical': bn}
        if bn:
            lm = X[:, f] <= sv
            rm = ~lm
            n['left'] = self.build_tree(X[lm], y[lm], d+1)
            n['right'] = self.build_tree(X[rm], y[rm], d+1)
        else:
            n['children'] = {}
            for v in np.unique(X[:, f]):
                m = X[:, f] == v
                n['children'][v] = self.build_tree(X[m], y[m], d+1)
        return n

    def predict(self, X):
        def _traverse(n, x):
            if isinstance(n, np.int64):
                return n
            if n['is_numerical']:
                return _traverse(n['left'], x) if x[n['feature']] <= n['split_val'] else _traverse(n['right'], x)
            return _traverse(n['children'].get(x[n['feature']], self.mc), x)
        return np.array([_traverse(self.tr, x) for x in X])

    def fit(self, X, y):
        self.tr = self.build_tree(X, y)
        self.mc = np.argmax(np.bincount(y))


    def evaluate(self, y_true, y_pred):
        tp, tn, fp, fn = np.sum((y_true == 1) & (y_pred == 1)), np.sum((y_true == 0) & (y_pred == 0)), np.sum((y_true == 0) & (y_pred == 1)), np.sum((y_true == 1) & (y_pred == 0))
        precision = tp / (tp + fp) if (tp + fp) > 0 else 0
        recall = tp / (tp + fn) if (tp + fn) > 0 else 0
        accuracy = (tp + tn) / (tp + tn + fp + fn) if (tp + tn + fp + fn) > 0 else 0
        f1 = 2 * (precision * recall) / (precision + recall) if (precision + recall) > 0 else 0

        return {"accuracy": accuracy, "precision": precision, "recall": recall, "f1": f1}

