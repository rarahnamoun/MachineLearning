import numpy as np
import pandas as pd
from collections import Counter

class DecisionTree:
    def __init__(self, max_depth=None, min_samples_split=2):
        self.md = max_depth
        self.ms = min_samples_split
        self.tr = None

    def calculate_entropy(self, y):
        class_counts = Counter(y)
        entropy = 0
        total_instances = len(y)
        for count in class_counts.values():
            probability = count / total_instances
            entropy -= probability * np.log2(probability)
        return entropy

    def calculate_gini(self, y):
        class_counts = Counter(y)
        probability = [count / len(y) for count in class_counts.values()]
        return 1 - np.sum(np.array(probability) ** 2)

    def information_gain(self, X, y, feature, criterion='entropy'):
        pc = self.calculate_entropy(y) if criterion == 'entropy' else self.calculate_gini(y)
        val, cnt = np.unique(X[:, feature], return_counts=True)
        wa = np.sum([
            (cnt[i] / np.sum(cnt)) * (self.calculate_entropy(y[X[:, feature] == val[i]]) if criterion == 'entropy' else self.calculate_gini(y[X[:, feature] == val[i]]))
            for i in range(len(val))
        ])
        return pc - wa

    def best_split(self, X, y):
        bf, bg = None, -1
        for f in range(X.shape[1]):
            g = self.information_gain(X, y, f)
            if g > bg:
                bg, bf = g, f
        return bf

    def build_tree(self, X, y, depth=0):
        if len(np.unique(y)) == 1 or len(y) < self.ms or (self.md and depth >= self.md):
            return np.argmax(np.bincount(y))

        f = self.best_split(X, y)
        if f is None:
            return np.argmax(np.bincount(y))

        tr = {f: {}}
        for v in np.unique(X[:, f]):
            sX, sY = X[X[:, f] == v], y[X[:, f] == v]
            tr[f][v] = self.build_tree(sX, sY, depth + 1)
        return tr

    def predict(self, X):
      def traverse(tree, instance):
          if not isinstance(tree, dict):
              return tree
          feature = list(tree.keys())[0]
          value = instance[feature]
          subtree = tree[feature].get(value, Counter(self.y_train).most_common(1)[0][0])
          return traverse(subtree, instance)

      return np.array([traverse(self.tr, x) for x in X])

