from sklearn.model_selection import train_test_split, GridSearchCV, RandomizedSearchCV
from sklearn.metrics import classification_report, confusion_matrix
from time import time
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
logger = logging.getLogger()

class KNNOptimizer:
    def __init__(self, X, y):
        self.X = X
        self.y = y
        self.X_train, self.X_test, self.y_train, self.y_test = train_test_split(X, y, test_size=0.3, random_state=42)
        self.best_model = None

    def grid_search(self):
        p_grid = {
            'n_neighbors': range(1, 51,5),
            'p': [1, 2]
        }
        gs = GridSearchCV(
            KNeighborsClassifier(algorithm='brute', metric='minkowski'),
            p_grid,
            cv=5,
            scoring='accuracy',
            n_jobs=6
        )
        logger.info("Starting GridSearchCV...")
        gs.fit(self.X_train, self.y_train)
        logger.info("GridSearchCV completed!")
        logger.info(f"Best Parameters: {gs.best_params_}")
        logger.info(f"Best Score: {gs.best_score_}")
        print(gs.best_params_)
        print(gs.best_score_)
        self.best_model = gs.best_estimator_
        return gs.best_params_, gs.best_score_

    def random_search(self):
        p_dist = {
            'n_neighbors': np.arange(1, 51),
            'p': [1, 2],

        }
        rs = RandomizedSearchCV(
            KNeighborsClassifier(algorithm='brute', metric='minkowski'),
            p_dist,
            n_iter=20,
            cv=5,
            scoring='accuracy',
            random_state=19,
            n_jobs=6
        )
        logger.info("Starting RandomizedSearchCV...")
        rs.fit(self.X_train, self.y_train)
        logger.info("RandomizedSearchCV completed!")
        logger.info(f"Best Parameters: {rs.best_params_}")
        logger.info(f"Best Score: {rs.best_score_}")
        print(rs.best_params_)
        print(rs.best_score_)
        self.best_model = rs.best_estimator_
        return rs.best_params_, rs.best_score_

    def evaluate_model(self):
        if self.best_model is None:
            raise ValueError("No model trained yet. Run grid_search() or random_search() first.")

        y_pred = self.best_model.predict(self.X_test)

        logger.info("Classification Report:")
        logger.info(classification_report(self.y_test, y_pred))

        cm = confusion_matrix(self.y_test, y_pred)
        plt.figure(figsize=(6, 4))
        sns.heatmap(cm, annot=True, fmt='d', cmap='Blues')
        plt.title('Confusion Matrix')
        plt.xlabel('Predicted')
        plt.ylabel('Actual')
        plt.show()




