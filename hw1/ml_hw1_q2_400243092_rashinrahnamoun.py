import numpy as np
from collections import Counter
from sklearn.metrics import accuracy_score, precision_score, recall_score, f1_score

class KNNClassifier:
    def __init__(self, neighbors=3):
        self._neighbors = neighbors
        self._features = None
        self._labels = None
        self._classes = None

    def fit(self, X, y):
        self._features = np.array(X, dtype=float)
        self._labels = np.array(y)
        self._classes = np.unique(y)
        return self

    def predict(self, X):
        if self._features is None or self._labels is None:
            raise ValueError("Model must be fitted before prediction")
        X = np.array(X, dtype=float)
        return np.array([self._predict_single(sample) for sample in X])

    def predict_proba(self, X):
        if self._features is None or self._labels is None:
            raise ValueError("Model must be fitted before prediction")
        X = np.array(X, dtype=float)
        return np.array([self._predict_proba_single(sample) for sample in X])

    def evaluate(self, X_test, y_test):
        y_pred = self.predict(X_test)
        return self._calculate_metrics(y_test, y_pred)

    def _predict_single(self, sample):
        sample = np.asarray(sample, dtype=float)
        distances = np.linalg.norm(self._features - sample, axis=1)
        nearest_indices = np.argpartition(distances, self._neighbors)[:self._neighbors]
        nearest_labels = self._labels[nearest_indices]
        return Counter(nearest_labels).most_common(1)[0][0]

    def _predict_proba_single(self, sample):
        sample = np.asarray(sample, dtype=float)
        distances = np.linalg.norm(self._features - sample, axis=1)
        nearest_indices = np.argpartition(distances, self._neighbors)[:self._neighbors]
        nearest_labels = self._labels[nearest_indices]
        counts = Counter(nearest_labels)
        return np.array([counts.get(cls, 0) / self._neighbors for cls in self._classes])

    def _calculate_metrics(self, y_true, y_pred):
        accuracy = accuracy_score(y_true, y_pred)
        precision = precision_score(y_true, y_pred, average="weighted", zero_division=0)
        recall = recall_score(y_true, y_pred, average="weighted", zero_division=0)
        f1 = f1_score(y_true, y_pred, average="weighted", zero_division=0)

        return {
            "accuracy": accuracy,
            "precision": precision,
            "recall": recall,
            "f1_score": f1
        }

    @property
    def neighbors(self):
        return self._neighbors

    @neighbors.setter
    def neighbors(self, value):
        if not isinstance(value, int) or value <= 0:
            raise ValueError("Number of neighbors must be a positive integer")
        self._neighbors = value

    @property
    def classes(self):
        return self._classes

    def __repr__(self):
        return f"KNNClassifier(neighbors={self._neighbors})"

